def fmt_close(v):
    """Format close price"""
    if v >= 10000:
        return f"{v:,.0f}"
    elif v >= 100:
        return f"{v:,.1f}"
    elif v >= 10:
        return f"{v:,.2f}"
    else:
        return f"{v:,.4f}"

def chg_class(v):
    if v > 0: return "up"
    if v < 0: return "down"
    return "flat"

def chg_fmt(v):
    return f"+{v:.2f}%" if v >= 0 else f"{v:.2f}%"

