# Process files
def process_all():
    dates_with_html = []
    for f in sorted(os.listdir(BASE)):
        if f.endswith('.html'):
            date_str = f.replace('.html', '')
            dates_with_html.append(date_str)

    processed = 0
    skipped = 0

    for date_str in dates_with_html:
        html_path = os.path.join(BASE, f"{date_str}.html")
        json_path = os.path.join(BASE, f"{date_str}_data.json")

        # Check if placeholder exists
        with open(html_path, 'r', encoding='utf-8') as f:
            content = f.read()

        if '<!-- STORY_CONTENT_PLACEHOLDER -->' not in content:
            skipped += 1
            continue

        # Load data
        if not os.path.exists(json_path):
            print(f"SKIP {date_str}: no data JSON")
            skipped += 1
            continue

        with open(json_path, 'r', encoding='utf-8') as f:
            data = json.load(f)

        if date_str not in DAILY_CONTEXT:
            print(f"SKIP {date_str}: no context defined")
            skipped += 1
            continue

        ctx = DAILY_CONTEXT[date_str]
        story_html = generate_story_html(date_str, ctx, data)

        new_content = content.replace('<!-- STORY_CONTENT_PLACEHOLDER -->', story_html)

        with open(html_path, 'w', encoding='utf-8') as f:
            f.write(new_content)

        processed += 1
        print(f"OK {date_str}")

    print(f"\nDone: {processed} processed, {skipped} skipped")


if __name__ == '__main__':
    process_all()
